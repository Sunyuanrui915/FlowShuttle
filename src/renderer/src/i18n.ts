export type I18nKey = keyof (typeof dictionaries)["zh-CN"];
export type Translator = (key: I18nKey) => string;

export const languageOptions: Array<{ value: LanguagePreference; labelKey: I18nKey }> = [
  { value: "zh-CN", labelKey: "languageSimplified" },
  { value: "zh-TW", labelKey: "languageTraditional" },
  { value: "en", labelKey: "languageEnglish" }
];

export function createTranslator(language: LanguagePreference): Translator {
  const dictionary = dictionaries[language] || dictionaries["zh-CN"];
  return (key) => dictionary[key] || dictionaries["zh-CN"][key] || key;
}
